#include <stdio.h>

int main()
{
	int a,b,c,count;
	a=0;
	b=0;
	c=0;
	count=0;
	for(a=500; a<=1000; a++){
		if(a*a+b*b==c*c){
			count++;
			printf("%2d:%5d%5d%5d\n",count,a,b,c);
		}
		for(b=501; b<=1000; b++){
			if(a*a+b*b==c*c){
				count++;
				printf("%2d:%5d%5d%5d\n",count,a,b,c);
			}
			for(c=502; c<=1000; c++){
				if(a*a+b*b==c*c){
					count++;
					printf("%2d:%5d%5d%5d\n",count,a,b,c);
				}
			}
		}
	}
	printf("直角三角形の総数 = %d\n",count);
	return 0;
}


