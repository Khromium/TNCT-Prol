#include <stdio.h>

int main()
{
	int n,j,i,k,m,l,o,p,q,r,s;
	printf("n=");
	scanf("%d",&n);
	m=(n+1)/2;
	for(i=0; i<m-1; i++){
		for(j=0; j<m-1-i; j++){
			printf("-");
		}
		for(l=0; l<1+2*i; l++){
			printf("*");
		}
		for(j=0; j<m-1-i; j++){
			printf("-");
		}
		printf("\n");
	}
	for(i=0; i<m; i++){
		for(j=0; j<i; j++){
			printf("-");
		}
		for(l=0; l<n-2*j; l++){
			printf("*");
		}
		for(o=0; o<j; o++){
			printf("-");
		}
		j=0;
		printf("\n");
	}
	return 0;
}


