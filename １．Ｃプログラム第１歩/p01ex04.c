#include <stdio.h>

int main()
{
	int x=17528,y=1482;
	printf(" %5d *%5d = %8d\n",x,y,x*y);
	printf(" %5d /%5d = %8d\n",x,y,x/y);
	printf(" %5d +%5d = %8d\n",x,y,x+y);
	printf(" %5d -%5d = %8d\n",x,y,x-y);
	printf(" %5d %%%5d = %8d\n",x,y,x%y);
	return 0;
}


